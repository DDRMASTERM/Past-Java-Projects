
public class Paper {
	
	private String writing;
	private int height;
	
	public Paper(String s, int h) {
		writing = s;
		height = h;
	}
	
	public String getWriting() {
		return writing;
	}
	
	public void setWriting(String s) {
		writing = s;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int h) {
		height = h;
	}

}
